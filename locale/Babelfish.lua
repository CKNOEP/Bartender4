local locale = {
	"deDE",
	"frFR",
	"esES",
	"zhCN",
	"zhTW",
	"koKR"
}
local files = {
	"ActionBarPrototype.lua",
	"ActionBars.lua",
	"ActionBarStates.lua",
	"BagBar.lua",
	"Bar.lua",
	"Bartender4.lua",
	"Button.lua",
	"ButtonBar.lua",
	"MicroMenu.lua",
	"Options.lua",
	"PetBar.lua",
	"StanceBar.lua",
}

local strings = {}

-- extract data from specified lua files
for idx,filename in pairs(files) do
	local file = io.open("../"..filename, "r")
	assert(file, "Could not open " .. filename)
	local text = file:read("*all")

	for match in string.gmatch(text, "L%[\"(.-)\"%]") do
		strings[match] = true
	end
end

